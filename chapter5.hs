{-
Exercise 1:
Consider two objets, a and b, in a category where both a and b are terminal objects.

Since a is terminal there is an (unique) arrow, say f, from b to a.
Since b is terminal, there is an (unique) arrow, say g, from a to b.
Finally, we have identity arrows for each object.

f . g = id of b and g . f = id of a

So any two terminal is isomorphic. That is, the terminal is unique up-to-isomorphism.
-}

